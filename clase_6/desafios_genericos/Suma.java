package clase_6.desafios_genericos;

public class Suma {
  public static int sumar(int a, int b) {
    return a + b;
  }

  public static double sumar(double a, double b) {
    return a + b;
  }

  public static long sumar(long a, long b) {
    return a + b;
  }

  public static float sumar(float a, float b) {
    return a + b;
  }
}